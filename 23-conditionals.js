let name = "Diego";

// Conditional Statement:
// If the value of 'name' is exactly equal to "Diego", execute the code inside the block.
if (name === "Diego") {
    console.log(`Hello ${name}`);
}
// If the value of 'name' is exactly equal to "Nico", execute the code inside this block.
else if (name === "Nico") {
    console.log(`Hello ${name}`);
}
// If none of the above conditions are met, execute the code inside this block.
else {
    console.log(`Name not found`);
}