// Definition of a list of activities.
let list = ["eat", "sleep", "code", "repeat"];

// "for" loop iterating over each element in the list.
// Starts with the variable i at 0, executes the code as long as i is less than the length of the list,
// and increments i by 1 in each iteration.
for (let i = 0; i < list.length; i++) {
    // Prints each element of the list at the current position to the console.
    console.log(list[i]);
}