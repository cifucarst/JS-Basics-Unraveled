/* The while loop is used to execute a block of code as long as a specific condition is 
true. The basic structure of the while loop is as follows: */

while (condition) {
    // Code to be executed while the condition is true
    // The condition is evaluated before each iteration
}

let counter = 0;

while (counter < 5) {
  console.log(`Iteration ${counter}`);
  counter++;
}